function link() {
    var link = document.getElementById("link").value;
    if (link == 1) {
        window.location.href = "http://www.vinhuni.edu.vn/";
    }
    else
        if (link == 2) {
            window.location.href = "https://nghean.gov.vn/";
        }
        else
            if (link == 3) {
                window.location.href = "https://www.microsoft.com/vi-vn/";
            }
            else
                window.location.href = "https://www.google.com/";
}
